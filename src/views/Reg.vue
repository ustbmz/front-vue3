  components: {
    Field,
    Form
  },

  setup () {
    const { state, getCaptcha, regHandle } = loginUtils()

    onMounted(async () => {
      getCaptcha()
    })
    const submit = async (value: RegInfo, form: any) => {
      console.log('üöÄ ~ file: Reg.vue ~ line 202 ~ submit ~ form', form)
      const ret = await regHandle()
